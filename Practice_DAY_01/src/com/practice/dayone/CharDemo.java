package com.practice.dayone;

public class CharDemo {

	public static void main(String[] args) {
		char var1 = 'a';
		System.out.println(var1);

		char var2 = 'A';
		System.out.println(var2);

		byte var3 = 65;
		System.out.println(var3);

		// small additions
		int ascii = var1;
		System.out.println(ascii);

		char ch = 66;
		System.out.println(ch);

	}

}
